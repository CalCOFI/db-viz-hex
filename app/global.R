# extract species names and date range ----
# all species (ichthyo + invert) are in the species table with WoRMS taxonomy
sp_names <- tbl(con, "species") |>
  left_join(
    tbl(con, "taxon") |>
      filter(authority == "WoRMS"),
    by = join_by(worms_id == taxonID)
  ) |>
  mutate(
    rank_part = ifelse(
      is.na(taxonRank) | taxonRank == "",
      "",
      paste0(tolower(taxonRank), ": ")),
    name_part = ifelse(
      is.na(common_name) | common_name == "",
      "",
      paste0(common_name, " ")),
    name = paste0(name_part, "(", rank_part, scientific_name, ")")
  ) |>
  pull(name) |>
  sort()

bio_date_rng <- tbl(con, "bio_obs") |>
  summarize(
    date_min = min(time_start, na.rm = TRUE) |> as.Date(),
    date_max = max(time_start, na.rm = TRUE) |> as.Date()
  ) |>
  collect() |>
  as.vector()

env_date_rng <- tbl(con, "env_obs") |>
  summarize(
    date_min = min(datetime_utc, na.rm = TRUE) |> as.Date(),
    date_max = max(datetime_utc, na.rm = TRUE) |> as.Date()
  ) |>
  collect() |>
  as.vector()

min_max_date <- c(
